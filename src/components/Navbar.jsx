const NavLinkItem = ({ to, label, icon, onClick, isMobile = false }) => {
  const location = useLocation();
  const isActive = location.pathname === to || (to !== '/' && location.pathname.startsWith(to));
  
  return (
    <NavLink
      to={to}
      className={`flex items-center px-4 py-3 text-sm transition-all duration-200 ${
        isActive 
          ? 'text-thPurple font-medium bg-thPurple/5' 
          : 'text-gray-700 hover:text-thPurple hover:bg-thPurple/5'
      } ${isMobile ? 'rounded-lg my-1' : 'rounded-md'}`}
      onClick={onClick}
    >
      <svg 
        className={`w-5 h-5 mr-3 ${isActive ? 'text-thPurple' : 'text-gray-400'}`} 
        fill="none" 
        viewBox="0 0 24 24" 
        stroke="currentColor"
      >
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={icon} />
      </svg>
      {label}
      {isActive && (
        <span className="ml-auto w-1.5 h-1.5 rounded-full bg-thPurple"></span>
      )}
    </NavLink>
  );
};

